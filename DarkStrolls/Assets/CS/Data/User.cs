using System.Collections;
using System.Collections.Generic;
using UnityEngine;

/// <summary>
/// Class to store user data.
/// </summary>
public class User
{
    /// <summary>
    /// Id of the user.
    /// </summary>
    public int Id { get; set; }

    /// <summary>
    /// Souls the user has.
    /// </summary>
    public int Souls { get; set; }

    /// <summary>
    /// Username of the user.
    /// </summary>
    public string Username { get; set; }
}
